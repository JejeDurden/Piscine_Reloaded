int		ft_count_if(char **tab, int (*f)(char *))
{
	int		result;
	int		i;

	i = 0;
	result = 0;
	while (tab[i] != '\0')
	{
		if (f(tab[i]) == 1)
			result++;
		i++;
	}
	return (result);
}
